
public class Demo {
    public static void main(String[] args) {
        int arr[] = new int[3];

        arr[0] = 1;
        arr[1] = 2;
        arr[2] = 3;

        for (int num : arr) { // This is For Each loop
            System.out.println(num);
        }

    }
}

