
public class ArithmeticOperators {
    public static void main(String args[]) {

        int num1 = 10;
        int num2 = 6;

        int result = 0;

        // add
        result = num1 + num2;

        // subtract
        result = num1 - num2;

        // multiply
        result = num1 * num2;

        // divide (/)
        result = num1 / num2;

        // divide (%)
        result = num1 % num2;

        // increment
        num1++;

        // decrement
        num2--;
    }
}

// Arithmetic Operators

// 1. + -> adds two numbers
// 2. - -> subtracts two numbers
// 3. * -> multiply two numbers
// 4. / -> Divide two numbers (Gives Quotient)
// 5. % -> Divide two numbers (Gives Remainder)
// 6. ++ -> Increment Operator (increments by 1)
// 7. -- -> Decrement Operator (decrements by 1)
