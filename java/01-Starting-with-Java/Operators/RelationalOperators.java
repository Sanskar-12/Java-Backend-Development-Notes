
public class RelationalOperators {
    public static void main(String[] args) {

        int num1 = 10;

        // ==
        boolean compare = num1 == 10;

        // !=
        compare = num1 != 10;

        // >
        compare = num1 > 10;

        // <
        compare = num1 < 10;

        // >=
        compare = num1 >= 10;

        // <=
        compare = num1 <= 10;
    }
}

// Relational Operators
// 1. == -> Double Equal to
// 2. != -> Not Equal to
// 3. > -> Greater than
// 4. < -> Less than
// 5. >= -> Greater than equal to
// 6. <= -> Less than equal to