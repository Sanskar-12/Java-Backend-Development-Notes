
public class AssigmentOperators {
    public static void main(String[] args) {

        // =
        int num1 = 10;

        // +=
        num1 += 10;

        // -=
        num1 -= 10;

        // *=
        num1 *= 10;

        // /=
        num1 /= 10;

        // %=
        num1 %= 10;
    }
}

// Assigment Operators

// 1. = -> Equal to
// 2. += -> Plus Equal to
// 3. -= -> Minus Equal to
// 4. *= -> Mulitply Equal to
// 5. /= -> Divide Equal to
// 6. %= -> Modulus Equal to
