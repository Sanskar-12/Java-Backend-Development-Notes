
public class TernaryOperator {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;

        // Ternary Operator
        boolean result = (num1 > num2) ? true
                : false;

        System.out.println(result); // result will be false
    }
}

// Ternary Operator

// (some exp) ? ( true ) : ( false ); --> looks like this
// so if the exp is true then it will print true or else false
