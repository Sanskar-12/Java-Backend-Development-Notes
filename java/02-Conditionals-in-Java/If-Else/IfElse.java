
public class IfElse {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;

        if (x > y) {
            System.out.println(x);
        } else {
            System.out.println(y); // this will be executed because 10 is not greater than 20
        }
    }
}

// If Else Statement

// if(exp) --> if the expression is true then the if statement will be executed
// else --> Or else statement will be executed
